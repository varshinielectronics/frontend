// Declaration
var number;

// Initialization
var number = 20;
console.log(`Value: ${number}`);

// Re-Initialization
var number = 35;
console.log(`New Value: ${number}`);

// Assignment
number = 50;
console.log(`Latest Value: ${number}`);

// let keyword allows declaration, initialization & value assignment
// But doesn't allow redeclaration
// Declaration
let fullName;

// Cannot redeclare the let variables
// let fullName = "Akshay";

// Initializing the let variable value
fullName = "Akshay";
console.log(`My first name is: ${fullName}`);

// Assigning the new value
fullName = "Akshay Rao";
console.log(`My full name is: ${fullName}`);

